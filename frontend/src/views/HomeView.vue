      seasonData,
      driversData,
      constructorsData,
      circuitsData,
      calendarData,
      driverStandingsData
    ] = await Promise.all([
      f1Api.getCurrentSeason(),
      f1Api.getDrivers(),
      f1Api.getConstructors(),
      f1Api.getCircuits(),
      f1Api.getCalendar(),
      f1Api.getDriverStandings()
    ]);

    // –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö
